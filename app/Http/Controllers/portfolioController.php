     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        request()->validate([
            "title" => 'required',
            "description" => 'required|min:5|max:255' 
        ]);

        Project::create([
            "title" => request('title'),
            "description" => request('description')
        ]);

        return redirect()->route('portfolio');
    }

